.include "my-macro"     # подключение файла с макроопределениями

.text

.global _start
_start:
        movl $5, %eax # задаётся число, факториал которог нужно найти
        movl $1, %ebx #временный результат

L1:
        cmpl  $0, %eax # сравниваем eax с нулем
        je    L2 # если равно нулю прыгаем в L2
        imull %eax, %ebx # умножаем ebx на eax и результат записываем в ebx
        decl  %eax # вычитается единица из eax
        jmp   L1 # прыгаем в начало L1

L2:
        Finish #завершаем программу
.end
